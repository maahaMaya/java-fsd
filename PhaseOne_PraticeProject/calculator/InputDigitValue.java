package calculator;

public class InputDigitValue {
	
	private float firstDigit;
	private float secondDigit;
	
	//Getter
	public float getFirstName() {
		return firstDigit;
	}
	public float getSecondName() {
		return secondDigit;
	}
	
	//Setter
	public void setFirstName(float firstDigit) {
		this.firstDigit = firstDigit;
	}
	public void setSecondName(float secondDigit) {
		this.secondDigit = secondDigit;
	}
}
